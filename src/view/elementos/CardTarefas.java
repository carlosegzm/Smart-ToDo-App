    private void botaoModernoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoModernoExcluirActionPerformed
        try {
            this.c.deleteTask(this.t.getId());
        } catch (SQLException | EntityNotFoundException ex) {
            Logger.getLogger(CardTarefas.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_botaoModernoExcluirActionPerformed

    private void botaoModernoMarcarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoModernoMarcarActionPerformed
        try {
            this.t.setConcluida(!this.t.getConcluida());
            this.c.updateTask(t);
        } catch (SQLException | EntityNotFoundException ex) {
            Logger.getLogger(CardTarefas.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_botaoModernoMarcarActionPerformed


    private void updateInfo(){
        this.jLabelNome.setText(this.t.getTitulo());
        this.jLabelDescricao.setText(this.t.getDescricao());
        this.jLabelCriacao.setText("Criada em " + this.t.getDataCriacao().toString());
        this.jLabelPrazo.setText("Prazo " +this.t.getPrazo().toString());
        this.jLabelPrioridade.setText("Prioridade " + this.t.getPrioridade());
        this.jLabelConcluida.setText(this.t.getConcluida() ? "Concluida" : "Em andamento");
    }
    
