    private void jButtonListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarActionPerformed
        try {
            ListaUsers lista = new ListaUsers(dashboard, c, u);
            dashboard.abrirPanelForm(lista);
        } catch (SQLException ex) {
            Logger.getLogger(EditarUser.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButtonListarActionPerformed

    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        try {
            this.u.setNome(jTextFieldNome.getText());
            this.u.setEmail(jTextFieldEmail.getText());
            this.dashboard.updateInfo();
            this.c.updateUser(this.u);
        } catch (SQLException | EntityNotFoundException ex) {
            Logger.getLogger(EditarUser.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

    private void jButtonExcluirContaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirContaActionPerformed
        try {
            c.deleteUser(this.u.getId());
            dashboard.dispose();
            Login login = new Login(c);
            login.pack();
            login.setVisible(true);
            login.setLocationRelativeTo(null);
            
        } catch (SQLException | EntityNotFoundException ex) {
            Logger.getLogger(EditarUser.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButtonExcluirContaActionPerformed

    private void initUserInfo(){
        this.jTextFieldNome.setText(this.u.getNome());
        this.jTextFieldEmail.setText(this.u.getEmail());
    }
    

